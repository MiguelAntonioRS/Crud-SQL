    private void chargeTable() {

        DefaultTableModel modelTable = (DefaultTableModel) tblStudents.getModel();
        modelTable.setRowCount(0);
        PreparedStatement ps;
        ResultSet rs;
        ResultSetMetaData rsmd;
        int columns;
        int width[] = {10, 30, 100, 100, 30};
        for (int i = 0; i < tblStudents.getColumnCount(); i++) {

            tblStudents.getColumnModel().getColumn(i).setPreferredWidth(width[i]);
        }

        try {
            Connection con = CConnection.getconec();
            ps = con.prepareStatement("SELECT id, matricula, nombre, email, edad"
                    + " FROM TBStudents WHERE activo=1;");
            rs = ps.executeQuery();
            rsmd = rs.getMetaData();
            columns = rsmd.getColumnCount();

            while (rs.next()) {

                Object[] row = new Object[columns];
                for (int index = 0; index < columns; index++) {
                    row[index] = rs.getObject(index + 1);
                }
                modelTable.addRow(row);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.toString());
        }

    }

