    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        String register = txtRegister.getText();
        String name = txtName.getText();
        String email = txtEmail.getText();
        int age = Integer.parseInt(txtAge.getText());
        String sex = null;
        if (rbMale.isSelected() == true) {

            sex = "M";
        } else if (rbFemale.isSelected() == true) {
            sex = "F";
        } else {
            JOptionPane.showMessageDialog(null, "Debe ingresar un sexo");
        }

        try {
            Connection con = CConnection.getconec();
            PreparedStatement ps = con.prepareStatement("INSERT INTO TBStudents (matricula, nombre, email, edad, sexo, activo)"
                    + " VALUES (?,?,?,?,?,?);");

            ps.setString(1, register);
            ps.setString(2, name);
            ps.setString(3, email);
            ps.setInt(4, age);
            ps.setString(5, sex);
            ps.setInt(6, 1);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Registro guardado");
            clean();
            chargeTable();

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.toString());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void tblStudentsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStudentsMouseClicked
        try {
            int row = tblStudents.getSelectedRow();
            int id = Integer.parseInt(tblStudents.getValueAt(row, 0).toString());
            Connection con = CConnection.getconec();
            PreparedStatement ps;
            ResultSet rs;

            ps = con.prepareStatement("SELECT *"
                    + "FROM TBStudents WHERE id=?");
            ps.setInt(1, id);
            rs = ps.executeQuery();

            while (rs.next()) {
                txtID.setText(String.valueOf(id));
                txtRegister.setText(rs.getString("matricula"));
                txtAge.setText(rs.getString("edad"));
                txtEmail.setText(rs.getString("email"));
                txtName.setText(rs.getString("nombre"));

                if (rs.getString("sexo").equals("M")) {
                    rbMale.setSelected(true);
                } else if (rs.getString("sexo").equals("M")) {
                    rbFemale.setSelected(true);
                }
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.toString());
        }
    }//GEN-LAST:event_tblStudentsMouseClicked

